package pkg100haringoding2;

public class Day81 {
    
    public static void main(String[] args) {
        int a = 90;
        float b = 14.67f;
        boolean c = true;
        char d = 'a';

        System.out.println("int ke String: " + String.valueOf(a));
        System.out.println("float ke String: " + String.valueOf(b));
        System.out.println("boolean ke String: " + String.valueOf(c));
        System.out.println("char ke String: " + String.valueOf(d));
    }
}
